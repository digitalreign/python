# Tuples: Immutable Sequences

# A tuple is an immutable sequence. 
# Should return 1212
print(12 * (100+1))
# 20190822 page 43
# Should return ('1', '2', '1', '2')
print(tuple('1212'))
# Should return (12, 100, 1)
print(tuple([12,100,1]))
# Should return ('J', 'a', 'n', 'u', 'a', 'r', 'y')
print(tuple('January'))
